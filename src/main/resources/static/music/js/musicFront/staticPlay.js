    function shuffleSwap(arr) {
        if(arr.length == 1) return arr;
        let i = arr.length;
        while(--i > 1) {
          let j = Math.floor(Math.random() * (i+1));
          /*
          //原始写法
          let tmp = arr[i];
          arr[i] = arr[j];
          arr[j] = tmp;
          */
          //es6的写法
          [arr[i], arr[j]] = [arr[j], arr[i]];
        }
        return arr;
    }

